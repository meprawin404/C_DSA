// 5. Write a C++ program to calculate the volume of a cuboid.

#include<iostream>
using namespace std;
int main()
{
    int lenght,width,height;
    cout<<"Enter the length: ";
    cin>>lenght;
    cout<<"Enter the width: ";
    cin>>width;
    cout<<"Enter the height: ";
    cin>>height;
    cout<<"The volume of cuboid is "<<lenght*width*height;
    return 0;
}
